package com.main;

/**
 *
 * @author hkouta
 */
public class UserInfo {


    String from;
    String msg;
   

    public UserInfo(String from, String msg) {
        this.from = from;
        this.msg = msg;

    }

    public UserInfo() {

    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    

}
